package bb.mcmc.analysis.glm;


public enum LinkFunction
{

        /**
         * Identity function.
         */

        IDENTITY,

        /**
         * Log function.
         */

        LOG,

        /**
         * Inverse function.
         */

        INVERSE,

        /**
         * Inverse square function.
         */

        INVERSE_SQUARE,

        /**
         * Square root function.
         */

        SQUARE_ROOT,

        /**
         * Logistic function.
         */

        LOGIT,

        /**
         * Probit function.
         */

        PROBIT,

        /**
         * Complementary log-log function.
         */

        COMPLEMENTARY_LOGLOG
}

